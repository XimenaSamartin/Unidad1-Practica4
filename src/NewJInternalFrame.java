    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        NewJPanel panel = new NewJPanel();

        int res = JOptionPane.showConfirmDialog(this, panel, "ESCRIBA VALORES A INSERTAR", JOptionPane.OK_OPTION);
        if(res == JOptionPane.OK_OPTION){
            String [] data = new String[] {panel.jTextField1.getText(), panel.jTextField2.getText(), panel.jFormattedTextField1.getText()};

            modelo.addRow(data);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditarMouseClicked
        //Revisando para ver si existe un renglon seleccionado de la JTable
        int renglon = jTable1.getSelectedRow();
        if(renglon == -1){
            JOptionPane.showMessageDialog(this, "ERROR!\n Para EDITAR debes SELECCIONAR primero el RENGLON  MODIFICAR");
            return;//Si entra al if el return interrumpe la ejecucion de codigo, por tanto aqui termina.
        }

        NewJPanel panel = new NewJPanel();
        panel.jTextField1.setText(modelo.getValueAt(renglon,0).toString());
        panel.jTextField2.setText(modelo.getValueAt(renglon,1).toString());
        panel.jFormattedTextField1.setText(modelo.getValueAt(renglon,2).toString());
       

        int respuesta = JOptionPane.showConfirmDialog(this, panel," ACTUALIZANDO VALORES ", JOptionPane.OK_OPTION);
        if(respuesta == JOptionPane.OK_OPTION){
            modelo.setValueAt(panel.jTextField1.getText(), renglon, 0);
            modelo.setValueAt(panel.jTextField2.getText(), renglon, 1);
            modelo.setValueAt(panel.jFormattedTextField1.getText(), renglon, 2);
            
        }
    }//GEN-LAST:event_btnEditarMouseClicked

