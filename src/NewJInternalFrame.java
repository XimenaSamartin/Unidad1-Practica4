    private void btnEliminarMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseReleased
        int renglon = jTable1.getSelectedRow();

        if(renglon == -1){
            JOptionPane.showMessageDialog(this, "ERROR \n Debes SELECCIONAR UN RENGLON PARA BORRARLO");
        }
        String cadena = "¿Estás seguro que deseas borrar a: "+modelo.getValueAt(renglon, 0).toString()+"?";
        int respuesta = JOptionPane.showConfirmDialog(this, cadena);
        if(respuesta == JOptionPane.OK_OPTION){
            modelo.removeRow(renglon);
        }
    }//GEN-LAST:event_btnEliminarMouseReleased

